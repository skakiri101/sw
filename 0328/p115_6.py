num1=25 #변수 num1에 47 배정
num2=5 #변수 num2에 18 배정

hap=num1+num2 #hap변수에 num1+num2 저장
cha=num1-num2 #cha변수에 num1-num2 저장
gob=num1*num2 #cha변수에 num1*num2 저장
nan=num1/num2 #nan변수에 num1/num2 저장

print("25과 5의 합은 : ",hap) #결과 출력
print("25과 5의 차은 : ",cha) #결과 출력
print("25과 5의 곱은 : ",gob) #결과 출력
print("25과 5의 나눗셈은 : ",nan) #결과 출력
